/*
1. Скобки и разделители
{ } — Фигурные скобки:
Определяют блок кода, тело класса/метода, границы областей видимости

( ) — Круглые скобки:
Используются в выражениях, вызовах методов, объявлении параметров, управляющих конструкциях

[ ] — Квадратные скобки:
Объявление массивов и доступ к элементам массива

< > — Угловые скобки:
Параметры обобщённых типов (Generics)

2. Арифметические операторы
+ — Сложение чисел или конкатенация строк

- — Вычитание

* — Умножение

/ — Деление

% — Остаток от деления (модуль)

3. Операторы сравнения
== — Для примитивов: сравнение значений
== — Для ссылочных типов: сравнение ссылок (один и тот же объект или нет)

!= — Неравенство

< — Меньше

> — Больше

<= — Меньше или равно

>= — Больше или равно

4. Логические операторы
&& — Логическое И (сокращённое)

|| — Логическое ИЛИ (сокращённое)

! — Логическое НЕ

& — И (без сокращения, вычисляет оба операнда)

| — ИЛИ (без сокращения, вычисляет оба операнда)

^ — Исключающее ИЛИ (XOR)

5. Побитовые операторы
~ — Побитовое отрицание

<< — Сдвиг влево

>> — Сдвиг вправо с сохранением знака

>>> — Сдвиг вправо без сохранения знака

& — Побитовое И

| — Побитовое ИЛИ

^ — Побитовое XOR

6. Операторы присваивания
= — Присваивание: присваивает значение правого операнда левому

+= — Присваивание со сложением: левый операнд = левый операнд + правый операнд

-= — Присваивание с вычитанием: левый операнд = левый операнд - правый операнд

*= — Присваивание с умножением: левый операнд = левый операнд * правый операнд

/= — Присваивание с делением: левый операнд = левый операнд / правый операнд

%= — Присваивание с остатком: левый операнд = левый операнд % правый операнд

<<= — Присваивание со сдвигом влево: левый операнд = левый операнд << правый операнд

>>= — Присваивание со сдвигом вправо: левый операнд = левый операнд >> правый операнд

>>>= — Присваивание с беззнаковым сдвигом вправо: левый операнд = левый операнд >>> правый операнд

&= — Присваивание с побитовым И: левый операнд = левый операнд & правый операнд

|= — Присваивание с побитовым ИЛИ: левый операнд = левый операнд | правый операнд

^= — Присваивание с побитовым XOR: левый операнд = левый операнд ^ правый операнд

7. Прочие символы
; — Точка с запятой:
Завершает инструкцию

, — Запятая:
Разделяет элементы в объявлениях, параметрах, операторе for

. — Точка:
Доступ к членам класса/объекта (полям, методам), разделение пакетов

... — Varargs:
Переменное количество аргументов в методах

?: — Тернарный оператор:
Условное выражение: условие ? значение1 : значение2

-> — Лямбда-стрелка:
Определяет лямбда-выражения

:: — Ссылка на метод:
Передача метода как аргумента

@ — Аннотация:
Добавление метаданных

* (в импорте) — Wildcard:
Импорт всех классов из пакета

8. Специальные случаи
++ / -- — Инкремент/декремент

+ (унарный) — Унарный плюс для чисел (по сути не меняет значение)

instanceof — Проверка типа объекта

new — Создание объекта

(type) — Приведение типа

9. ESCAPE-ПОСЛЕДОВАТЕЛЬНОСТИ В СТРОКАХ
\n — Перевод строки (new line)

\t — Табуляция

\\ — Обратный слеш

\" — Двойная кавычка

\' — Одинарная кавычка

\r — Возврат каретки

\b — Backspace

\f — Перевод страницы

\u0000 — Unicode символ

10. ДОПОЛНИТЕЛЬНЫЕ СИМВОЛЫ
: — Двоеточие (используется в тернарном операторе, switch, labels)

? — Вопросительный знак (тернарный оператор, wildcard в Generics)

\ — Обратный слеш (escape-последовательности)

_ — Нижнее подчёркивание (в числовых литералах: 1_000_000)

$ — Знак доллара (разрешён в именах, но не рекомендуется)
 */
